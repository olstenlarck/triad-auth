import { createHash } from "node:crypto";
import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import { dirname, extname, resolve } from "node:path";

const distDirectory = resolve("dist");
const outputFile = resolve("src/generated/csp-script-hashes.ts");

async function collectHtmlFiles(directory) {
  const files = [];
  const entries = await readdir(directory, { withFileTypes: true });
  for (const entry of entries.sort((a, b) => a.name.localeCompare(b.name))) {
    const path = resolve(directory, entry.name);
    if (entry.isDirectory()) files.push(...await collectHtmlFiles(path));
    else if (entry.isFile() && extname(entry.name).toLowerCase() === ".html") files.push(path);
  }
  return files;
}

const htmlFiles = await collectHtmlFiles(distDirectory);
const hashes = new Set();
for (const file of htmlFiles) {
  const html = await readFile(file, "utf8");
  for (const match of html.matchAll(/<script\b([^>]*)>([\s\S]*?)<\/script\s*>/gi)) {
    const [, attributes, body] = match;
    if (/(?:^|\s)src(?:\s*=|\s|$)/i.test(attributes)) continue;
    hashes.add(`'sha256-${createHash("sha256").update(body, "utf8").digest("base64")}'`);
  }
}

const sortedHashes = [...hashes].sort();
const output = `// Generated by scripts/generate-csp-hashes.mjs. Do not edit.\nexport const cspScriptHashes = ${JSON.stringify(sortedHashes, null, 2)} as const;\n`;
await mkdir(dirname(outputFile), { recursive: true });
await writeFile(outputFile, output);
console.log(`Generated ${sortedHashes.length} CSP script hashes from ${htmlFiles.length} HTML files.`);
